class User{
    constructor(id, name, last_name, email, number_phone, date_birthday, role){
        this.id = id
        this.name = name
        this.last_name = last_name
        this.email = email
        this.number_phone = number_phone
        this.date_birthday = date_birthday
        this.role = role
    }
}

module.exports = User